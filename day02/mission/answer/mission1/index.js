let animal = 'cat';

switch (animal) {
    case 'dog':
        console.log('강아지');
        break;
    case 'tiger':
        console.log('호랑이');
        break;
    case 'cat':
        console.log('고양이');
        break;
    case 'lion':
        console.log('사자');
        break;
    default:
        console.log('참새');
}
